    static public int countCharacters(String[] words, String chars) {

        StringBuilder stringBuilder = new StringBuilder();

        Arrays.stream(words).forEach(s -> {
            match = true;
            StringBuilder tempChars = new StringBuilder(chars);
            s.chars().forEach(c -> {
                int pos = tempChars.indexOf(String.valueOf((char)c));
                if (pos < 0) {
                    match = false;
                } else {
                    tempChars.deleteCharAt(pos);
                }
            });
            if (match) {
                stringBuilder.append(s);
            }
        });

        return stringBuilder.length();
    }

